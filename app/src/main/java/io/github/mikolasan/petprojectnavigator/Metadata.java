package io.github.mikolasan.petprojectnavigator;

public class Metadata {

    String provenance; // indicates how the data was obtained.
    String trust; // describes for which purposes the data can be relied upon.
    String context; // logical grouping for the data items.
    String permissions; // detail who can read and write the data.
    String performance; // an expectation that the data will often be used together.

}
